#include <stdio.h>

/**
 * add - adds x to y
 * @x:1st number
 * @y: 2nd number
 * Return: Always (success) 0
 */
int add(int x, int y)
{
	return (x + y);
}

/**
 * sub - subractts y from x
 * @x:1st number
 * @y: 2nd number
 * Return: Always (success) 0
 */
int sub(int x, int y)
{
	return (x - y);
}

/**
 * mult - multiply x by y
 * @x:1st number
 * @y: 2nd number
 * Return: Always (success) 0
 */
int mult(int x, int y)
{
	return (x * y);
}

/**
 * div - divide x by y
 * @x:1st number
 * @y: 2nd number
 * Return: Always (success) 0
 */
int div(int x, int y)
{
	return (x / y);
}

/**
 * mod - remainder of x div y
 * @x:1st number
 * @y: 2nd number
 * Return: Always (success) 0
 */
int mod(int x, int y)
{
	return (x % y);
}
